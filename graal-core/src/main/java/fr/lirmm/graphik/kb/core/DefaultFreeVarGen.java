package fr.lirmm.graphik.kb.core;

import fr.lirmm.graphik.kb.SymbolGenerator;

public class DefaultFreeVarGen implements SymbolGenerator {

	private String prefix;
	private int index;

	// /////////////////////////////////////////////////////////////////////////
	// CONSTRUCTOR
	// /////////////////////////////////////////////////////////////////////////

	public DefaultFreeVarGen(String prefix) {
		this.index = 0;
		this.prefix = prefix;
	}

	// /////////////////////////////////////////////////////////////////////////
	// PUBLIC METHODS
	// /////////////////////////////////////////////////////////////////////////

	@Override
	public Term getFreeVar() {
		return new Term(this.prefix + getFreeIndex(), Term.Type.VARIABLE);
	}

	// /////////////////////////////////////////////////////////////////////////
	// PRIVATE METHODS
	// /////////////////////////////////////////////////////////////////////////

	private int getFreeIndex() {
		return index++;
	}

};
